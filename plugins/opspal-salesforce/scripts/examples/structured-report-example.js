/**
 * Structured Report Example
 *
 * Demonstrates using StructuredFormatter to create beautiful,
 * Claude Code-friendly reports with tables, code blocks, metrics,
 * and status indicators.
 *
 * This example shows a typical Salesforce deployment report using
 * the structured content formatter library.
 *
 * Usage:
 *   node structured-report-example.js
 *
 * @version 1.0.0
 * @date 2025-11-04
 * @feature Claude Code v2.0.32 Integration
 */

const path = require('path');
const { StructuredFormatter } = require('../lib/structured-content-formatter');

class DeploymentReporter {
  constructor() {
    this.formatter = new StructuredFormatter();
  }

  /**
   * Generate a complete deployment report
   */
  generateReport(deploymentData) {
    let output = '';

    // Title section
    output += this.formatter.section(
      `Deployment Report - ${deploymentData.orgName}`,
      { level: 1, emoji: '🚀' }
    );
    output += '\n';

    // Summary box
    output += this.formatter.summaryBox(
      'Deployment Summary',
      [
        `Org: ${deploymentData.orgName} (${deploymentData.orgId})`,
        `Status: ${this.formatter.statusBadge(deploymentData.status, deploymentData.statusText)}`,
        `Duration: ${deploymentData.duration}`,
        `API Version: ${deploymentData.apiVersion}`,
        `Timestamp: ${deploymentData.timestamp}`
      ],
      { type: 'summary' }
    );
    output += '\n\n';

    // Component stats table
    output += this.formatter.section('Component Statistics', { type: 'metrics', level: 2 });
    output += this.formatter.table(
      deploymentData.components,
      {
        type: 'Component Type',
        count: 'Count',
        deployed: 'Deployed',
        failed: 'Failed',
        status: 'Status'
      }
    );
    output += '\n\n';

    // Test results
    if (deploymentData.tests && deploymentData.tests.length > 0) {
      output += this.formatter.section('Test Results', { type: 'testing', level: 2 });
      output += this.formatter.metricsSummary({
        'Tests Run': deploymentData.testsRun,
        'Tests Passed': deploymentData.testsPassed,
        'Tests Failed': deploymentData.testsFailed,
        'Code Coverage': `${deploymentData.codeCoverage}%`,
        'Test Duration': deploymentData.testDuration
      });
      output += '\n\n';

      // Failed tests table (if any)
      if (deploymentData.failedTests && deploymentData.failedTests.length > 0) {
        output += this.formatter.warningBox(
          `${deploymentData.failedTests.length} test(s) failed`,
          deploymentData.failedTests.map(t => `${t.name}: ${t.error}`)
        );
        output += '\n\n';
      }
    }

    // Deployment timeline
    output += this.formatter.section('Deployment Timeline', { type: 'progress', level: 2 });
    output += this.formatter.timeline(deploymentData.timeline);
    output += '\n\n';

    // Warnings (if any)
    if (deploymentData.warnings && deploymentData.warnings.length > 0) {
      output += this.formatter.section('Warnings', { type: 'warnings', level: 2 });
      output += this.formatter.bulletList(deploymentData.warnings, { emoji: '⚠️' });
      output += '\n\n';
    }

    // Next steps
    output += this.formatter.section('Next Steps', { type: 'info', level: 2 });
    output += this.formatter.numberedList([
      'Verify component functionality in the target org',
      'Run smoke tests on critical business processes',
      'Monitor error logs for the next 24 hours',
      'Update deployment documentation',
      'Notify stakeholders of successful deployment'
    ]);
    output += '\n\n';

    // Deployment command for reference
    output += this.formatter.section('Deployment Command', { type: 'api', level: 2 });
    output += this.formatter.codeBlock(
      deploymentData.command,
      'bash',
      { title: 'Command used for deployment' }
    );
    output += '\n';

    // Footer
    output += '\n' + '─'.repeat(80) + '\n';
    output += `Generated by: Deployment Reporter v1.0\n`;
    output += `Report Date: ${new Date().toISOString()}\n`;
    output += `🤖 Generated with Claude Code - Structured Content Formatter\n`;

    return output;
  }
}

