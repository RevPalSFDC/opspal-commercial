// Backup / restore
// ---------------------------------------------------------------------------

function ensureDir(dir) {
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }
}

/**
 * Create a timestamped backup of CLAUDE.md
 */
function createBackup(claudeMdPath, opts = {}) {
  const { label = '', trigger = 'sync' } = opts;

  if (!fs.existsSync(claudeMdPath)) {
    return null;
  }

  const backupDirPath = getBackupDir();
  ensureDir(backupDirPath);

  const now = new Date();
  const timestamp = now.toISOString().replace(/[:.]/g, '-').replace('T', '-').slice(0, 19);
  const suffix = label ? `-${label.replace(/[^a-z0-9-]/gi, '-').toLowerCase()}` : '';
  const backupName = `${timestamp}${suffix}.md`;
  const backupPath = path.join(backupDirPath, backupName);

  fs.copyFileSync(claudeMdPath, backupPath);

  // Update manifest
  const manifestPath = path.join(backupDirPath, 'manifest.json');
  let manifest = [];
  if (fs.existsSync(manifestPath)) {
    try {
      manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
    } catch (_e) {
      manifest = [];
    }
  }

  manifest.push({
    file: backupName,
    timestamp: now.toISOString(),
    label: label || null,
    trigger,
    source: claudeMdPath
  });

  // Prune old backups
  if (manifest.length > MAX_BACKUPS) {
    const toRemove = manifest.splice(0, manifest.length - MAX_BACKUPS);
    for (const entry of toRemove) {
      const oldPath = path.join(backupDirPath, entry.file);
      try { fs.unlinkSync(oldPath); } catch (_e) { /* ignore */ }
    }
  }

  fs.writeFileSync(manifestPath, JSON.stringify(manifest, null, 2), 'utf8');
  return backupPath;
}

/**
 * List available backups
 */
function listBackups() {
  const backupDirPath = getBackupDir();
  const manifestPath = path.join(backupDirPath, 'manifest.json');
  if (!fs.existsSync(manifestPath)) {
    return [];
  }

  try {
    const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
    return manifest.map(entry => ({
      path: path.join(backupDirPath, entry.file),
      timestamp: entry.timestamp,
      label: entry.label,
      trigger: entry.trigger,
      exists: fs.existsSync(path.join(backupDirPath, entry.file))
    })).filter(b => b.exists);
  } catch (_e) {
    return [];
  }
}

/**
 * Restore a backup to the CLAUDE.md path
 */
function restoreBackup(backupPath, claudeMdPath) {
  if (!fs.existsSync(backupPath)) {
    throw new Error(`Backup not found: ${backupPath}`);
  }

  // Backup the current file first (before-rollback snapshot)
  if (fs.existsSync(claudeMdPath)) {
    createBackup(claudeMdPath, { label: 'before-rollback', trigger: 'rollback' });
  }

  fs.copyFileSync(backupPath, claudeMdPath);
}

// ---------------------------------------------------------------------------
// Pending review support
// ---------------------------------------------------------------------------

/**
 * Write a pending review manifest for deferred merges
 */
function writePendingReview(data) {
  const pendingDirPath = getPendingDir();
  ensureDir(pendingDirPath);
  const timestamp = new Date().toISOString().replace(/[:.]/g, '-').replace('T', '-').slice(0, 19);
  const pendingPath = path.join(pendingDirPath, `${timestamp}.json`);
  fs.writeFileSync(pendingPath, JSON.stringify(data, null, 2), 'utf8');
  return pendingPath;
}

/**
 * Read the most recent pending review, if any
 */
function readPendingReview() {
  const pendingDirPath = getPendingDir();
  if (!fs.existsSync(pendingDirPath)) return null;

  const files = fs.readdirSync(pendingDirPath)
    .filter(f => f.endsWith('.json'))
    .sort()
    .reverse();

  if (files.length === 0) return null;

  try {
    const data = JSON.parse(fs.readFileSync(path.join(pendingDirPath, files[0]), 'utf8'));
    data._file = files[0];
    return data;
  } catch (_e) {
    return null;
  }
}

/**
 * Clear pending reviews
 */
function clearPendingReview() {
  const pendingDirPath = getPendingDir();
  if (!fs.existsSync(pendingDirPath)) return;

  const files = fs.readdirSync(pendingDirPath).filter(f => f.endsWith('.json'));
  for (const file of files) {
    try { fs.unlinkSync(path.join(pendingDirPath, file)); } catch (_e) { /* ignore */ }
  }
}

