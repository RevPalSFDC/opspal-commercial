'use strict';

/**
 * claudemd-section-parser.js - Section-level ownership parser and merge engine
 *
 * Parses CLAUDE.md files into managed (plugin-owned) and user-owned sections,
 * then merges new plugin content without destroying user customizations.
 *
 * Marker types:
 *   <!-- OPSPAL_MANAGED section="name" version="x.y.z" checksum="abc123" -->
 *   <!-- OPSPAL_MANAGED_END section="name" -->
 *
 *   <!-- USER_SECTION name="name" -->
 *   <!-- USER_SECTION_END -->
 *
 *   <!-- USER_EDITABLE_START name="name" -->  (legacy alias for USER_SECTION)
 *   <!-- USER_EDITABLE_END -->
 *
 * @version 1.0.0
 */

const crypto = require('crypto');
const fs = require('fs');
const path = require('path');
const os = require('os');

// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

const MAX_BACKUPS = 10;

// Resolve lazily so tests can override HOME before calling
function homeDir() {
  return process.env.HOME || os.homedir();
}

function getBackupDir() {
  return path.join(homeDir(), '.claude', 'opspal', 'claudemd-backups');
}

function getPendingDir() {
  return path.join(homeDir(), '.claude', 'opspal', 'claudemd-pending');
}

// Known plugin-generated heading patterns (used for legacy detection)
const KNOWN_PLUGIN_HEADINGS = [
  /^## 🚨 CRITICAL: Agent Routing Rules/,
  /^## 📋 Project Overview/,
  /^## 🔌 Installed Plugins/,
  /^## 📋 Work-Index Auto-Capture/,
  /^## 📁 Project Structure/,
  /^## 🚀 Quick Start/,
  /^## 🎯 AGENT-FIRST PROTOCOL/,
  /^## 🔄 Common Workflows/,
  /^## 📂 Working with Customer Instances/,
  /^## 🔒 Security/,
  /^## 🔧 Troubleshooting/,
  /^## 🎨 Brand Gallery/,
  /^\*\*Generated by \/sync-claudemd\*\*/
];

// Regex patterns for markers
const MANAGED_START_RE = /^<!-- OPSPAL_MANAGED section="([^"]+)" version="([^"]*)" checksum="([^"]*)" -->\s*$/;
const MANAGED_END_RE = /^<!-- OPSPAL_MANAGED_END(?:\s+section="[^"]*")? -->\s*$/;
const USER_SECTION_START_RE = /^<!-- USER_SECTION name="([^"]+)" -->\s*$/;
const USER_SECTION_END_RE = /^<!-- USER_SECTION_END -->\s*$/;
const LEGACY_USER_START_RE = /^<!-- USER_EDITABLE_START name="([^"]+)" -->\s*$/;
const LEGACY_USER_END_RE = /^<!-- USER_EDITABLE_END -->\s*$/;

// ---------------------------------------------------------------------------
// Checksum
// ---------------------------------------------------------------------------

/**
 * Compute a 12-char hex checksum for section content
 */
function computeChecksum(content) {
  return crypto.createHash('sha256').update(content.trim()).digest('hex').slice(0, 12);
}

// ---------------------------------------------------------------------------
// Marker generation
// ---------------------------------------------------------------------------

/**
 * Wrap content in OPSPAL_MANAGED markers
 */
function generateManagedBlock(name, content, version) {
  const checksum = computeChecksum(content);
  return [
    `<!-- OPSPAL_MANAGED section="${name}" version="${version}" checksum="${checksum}" -->`,
    content,
    `<!-- OPSPAL_MANAGED_END section="${name}" -->`
  ].join('\n');
}

/**
 * Wrap content in USER_SECTION markers
 */
function generateUserSectionBlock(name, content) {
  return [
    `<!-- USER_SECTION name="${name}" -->`,
    content,
    `<!-- USER_SECTION_END -->`
  ].join('\n');
}

// ---------------------------------------------------------------------------
// Parser
// ---------------------------------------------------------------------------

/**
 * Parse a CLAUDE.md file into an ordered array of typed sections.
 *
 * Returns: [{ type, name?, content, version?, checksum? }, ...]
 *   type: 'managed' | 'user-section' | 'user-verbatim'
 */
function parseSections(content) {
  if (!content || !content.trim()) {
    return [];
  }

  const lines = content.split('\n');
  const sections = [];
  let i = 0;
  let verbatimBuffer = [];

  function flushVerbatim() {
    if (verbatimBuffer.length > 0) {
      const text = verbatimBuffer.join('\n');
      // Only emit non-empty verbatim sections
      if (text.trim()) {
        sections.push({ type: 'user-verbatim', content: text });
      }
      verbatimBuffer = [];
    }
  }

  while (i < lines.length) {
    const line = lines[i];

    // Check for OPSPAL_MANAGED start
    const managedMatch = line.match(MANAGED_START_RE);
    if (managedMatch) {
      flushVerbatim();
      const name = managedMatch[1];
      const version = managedMatch[2];
      const checksum = managedMatch[3];
      const bodyLines = [];
      i++;
      while (i < lines.length && !MANAGED_END_RE.test(lines[i])) {
        bodyLines.push(lines[i]);
        i++;
      }
      sections.push({
        type: 'managed',
        name,
        version,
        checksum,
        content: bodyLines.join('\n')
      });
      i++; // skip end marker
      continue;
    }

    // Check for USER_SECTION start
    const userMatch = line.match(USER_SECTION_START_RE);
    if (userMatch) {
      flushVerbatim();
      const name = userMatch[1];
      const bodyLines = [];
      i++;
      while (i < lines.length && !USER_SECTION_END_RE.test(lines[i])) {
        bodyLines.push(lines[i]);
        i++;
      }
      sections.push({
        type: 'user-section',
        name,
        content: bodyLines.join('\n')
      });
      i++; // skip end marker
      continue;
    }

    // Check for legacy USER_EDITABLE_START
    const legacyMatch = line.match(LEGACY_USER_START_RE);
    if (legacyMatch) {
      flushVerbatim();
      const name = legacyMatch[1];
      const bodyLines = [];
      i++;
      while (i < lines.length && !LEGACY_USER_END_RE.test(lines[i])) {
        bodyLines.push(lines[i]);
        i++;
      }
      sections.push({
        type: 'user-section',
        name,
        content: bodyLines.join('\n')
      });
      i++; // skip end marker
      continue;
    }

    // Otherwise accumulate as verbatim
    verbatimBuffer.push(line);
    i++;
  }

  flushVerbatim();
  return sections;
}

// ---------------------------------------------------------------------------
// Legacy format detection
// ---------------------------------------------------------------------------

/**
 * Detect what marker types are present in a CLAUDE.md
 */
function detectLegacyFormat(content) {
  if (!content) {
    return {
      hasMarkers: false,
      hasManagedMarkers: false,
      hasUserMarkers: false,
      hasLegacyMarkers: false,
      userContentDetected: false
    };
  }

  const hasManagedMarkers = /<!-- OPSPAL_MANAGED section="/.test(content);
  const hasUserMarkers = /<!-- USER_SECTION name="/.test(content);
  const hasLegacyMarkers = /<!-- USER_EDITABLE_START name="/.test(content);
  const hasMarkers = hasManagedMarkers || hasUserMarkers || hasLegacyMarkers;

  // Detect user content: headings that don't match known plugin headings
  let userContentDetected = false;
  if (!hasManagedMarkers) {
    const headingLines = content.split('\n').filter(l => /^## /.test(l));
    for (const heading of headingLines) {
      const isKnown = KNOWN_PLUGIN_HEADINGS.some(re => re.test(heading));
      if (!isKnown) {
        userContentDetected = true;
        break;
      }
    }
  }

  return {
    hasMarkers,
    hasManagedMarkers,
    hasUserMarkers,
    hasLegacyMarkers,
    userContentDetected
  };
}

// ---------------------------------------------------------------------------
// Merge engine
// ---------------------------------------------------------------------------

/**
 * Merge existing CLAUDE.md sections with newly generated plugin content.
 *
 * @param {Array} existingSections - from parseSections(existingContent)
 * @param {Map<string, {content: string, version: string}>} generatedSections - new plugin content by section name
 * @param {Object} options
 * @param {string} options.mode - 'interactive' | 'non-interactive'
 * @param {boolean} options.force - overwrite even on conflict
 * @returns {{ merged: string, changes: Array, conflicts: Array, skipped: Array, isClean: boolean, hasUserVerbatim: boolean }}
 */
function mergeClaudeMd(existingSections, generatedSections, options = {}) {
  const { mode = 'interactive', force = false } = options;
  const changes = [];
  const conflicts = [];
  const skipped = [];
  let hasUserVerbatim = false;

  // Track which generated sections have been placed
  const placed = new Set();
  const result = [];

  for (const section of existingSections) {
    if (section.type === 'user-verbatim') {
      hasUserVerbatim = true;
      skipped.push({ type: 'user-verbatim', preview: section.content.trim().slice(0, 120) });
      result.push(section.content);
      continue;
    }

    if (section.type === 'user-section') {
      skipped.push({ type: 'user-section', name: section.name });
      // Re-emit with markers (use legacy format if it was originally legacy, otherwise new format)
      result.push(generateUserSectionBlock(section.name, section.content));
      continue;
    }

    if (section.type === 'managed') {
      const newSection = generatedSections.get(section.name);
      placed.add(section.name);

      if (!newSection) {
        // Plugin dropped this section
        changes.push({ action: 'removed', name: section.name });
        // Don't include it in output
        continue;
      }

      const newChecksum = computeChecksum(newSection.content);
      const currentContentChecksum = computeChecksum(section.content);

      // Check if user modified inside managed block
      const userModified = section.checksum && section.checksum !== currentContentChecksum;

      if (userModified && !force) {
        // Conflict: user edited a managed section
        conflicts.push({
          name: section.name,
          userContent: section.content,
          pluginContent: newSection.content
        });

        if (mode === 'non-interactive') {
          // Keep user's version, don't overwrite
          result.push(generateManagedBlock(section.name, section.content, section.version));
          continue;
        }
        // In interactive mode, prefer plugin content (caller handles conflict presentation)
        result.push(generateManagedBlock(section.name, newSection.content, newSection.version));
        changes.push({ action: 'updated-conflict', name: section.name });
        continue;
      }

      if (section.checksum === newChecksum) {
        // No change
        result.push(generateManagedBlock(section.name, section.content, section.version));
        continue;
      }

      // Normal update
      result.push(generateManagedBlock(section.name, newSection.content, newSection.version));
      changes.push({
        action: 'updated',
        name: section.name,
        oldVersion: section.version,
        newVersion: newSection.version
      });
    }
  }

  // Append any new generated sections not yet placed
  for (const [name, section] of generatedSections) {
    if (!placed.has(name)) {
      result.push(generateManagedBlock(name, section.content, section.version));
      changes.push({ action: 'added', name });
    }
  }

  const merged = result.join('\n\n');
  const isClean = conflicts.length === 0 && !hasUserVerbatim;

  return { merged, changes, conflicts, skipped, isClean, hasUserVerbatim };
}

// ---------------------------------------------------------------------------
// Diff report
// ---------------------------------------------------------------------------

/**
 * Format a human-readable diff report from merge results
 */
function formatDiffReport(mergeResult) {
  const { changes, conflicts, skipped, isClean, hasUserVerbatim } = mergeResult;
  const lines = [];

  lines.push('');
  lines.push('  CLAUDE.md Sync Report');
  lines.push('  ' + '='.repeat(40));
  lines.push('');

  // Changes
  for (const change of changes) {
    switch (change.action) {
      case 'updated':
        if (change.oldVersion !== change.newVersion) {
          lines.push(`  [WILL UPDATE]  ${change.name}  ${change.oldVersion} -> ${change.newVersion}`);
        } else {
          lines.push(`  [WILL UPDATE]  ${change.name}  (content changed)`);
        }
        break;
      case 'updated-conflict':
        lines.push(`  [CONFLICT]     ${change.name}  (user edits overridden by plugin update)`);
        break;
      case 'added':
        lines.push(`  [NEW]          ${change.name}`);
        break;
      case 'removed':
        lines.push(`  [REMOVED]      ${change.name}`);
        break;
    }
  }

  // Preserved sections
  for (const skip of skipped) {
    if (skip.type === 'user-section') {
      lines.push(`  [PRESERVED]    ${skip.name}  (user section)`);
    } else if (skip.type === 'user-verbatim') {
      const preview = skip.preview.replace(/\n/g, ' ').slice(0, 60);
      lines.push(`  [UNPROTECTED]  "${preview}..."  (not in markers)`);
    }
  }

  // Conflicts
  if (conflicts.length > 0) {
    lines.push('');
    lines.push(`  WARNING: ${conflicts.length} conflict(s) — user edits inside managed sections`);
  }

  // Summary
  lines.push('');
  const updatedCount = changes.filter(c => c.action === 'updated' || c.action === 'updated-conflict').length;
  const addedCount = changes.filter(c => c.action === 'added').length;
  const removedCount = changes.filter(c => c.action === 'removed').length;
  const preservedCount = skipped.filter(s => s.type === 'user-section').length;
  const unprotectedCount = skipped.filter(s => s.type === 'user-verbatim').length;

  lines.push(`  Summary: ${updatedCount} updated, ${addedCount} new, ${removedCount} removed, ${preservedCount} preserved`);

  if (unprotectedCount > 0) {
    lines.push(`  ${unprotectedCount} unprotected user section(s) — wrap in USER_SECTION markers to protect`);
  }

  if (isClean) {
    lines.push('');
    lines.push('  Status: Clean merge — all user content is protected.');
  } else if (hasUserVerbatim) {
    lines.push('');
    lines.push('  Status: User content outside markers detected — review recommended.');
  }

  lines.push('');
  return lines.join('\n');
}

