function validateNextBestActions() {
  const report = JSON.parse(readText('reports/exec/opspal-next-best-actions.json'));
  if (!Array.isArray(report.actions) || report.actions.length < 5) {
    fail('Next-best-action report must contain at least 5 actions.');
    return;
  }

  let previous = Number.POSITIVE_INFINITY;
  for (const action of report.actions) {
    const required = [
      'action_id',
      'source_opportunity_id',
      'title',
      'source_plugin',
      'risk_class',
      'confidence',
      'triage_label',
      'triage_shadow_mode',
      'rank_score',
      'approval_required',
      'rollback_plan',
      'artifacts',
    ];
    for (const field of required) {
      if (action[field] == null || action[field] === '') {
        fail(`Next-best-action missing required field "${field}" for ${action.action_id || 'unknown'}.`);
      }
    }

    if (!['low', 'medium', 'high', 'critical'].includes(action.risk_class)) {
      fail(`Invalid risk_class "${action.risk_class}" for ${action.action_id}.`);
    }
    if (typeof action.confidence !== 'number' || action.confidence < 0 || action.confidence > 1) {
      fail(`Invalid confidence for ${action.action_id}.`);
    }
    if (!['auto_route', 'assisted_review', 'manual_review'].includes(action.triage_label)) {
      fail(`Invalid triage_label "${action.triage_label}" for ${action.action_id}.`);
    }
    if (action.triage_shadow_mode !== true) {
      fail(`Action ${action.action_id} must remain in shadow triage mode.`);
    }
    if (typeof action.rank_score !== 'number') {
      fail(`Invalid rank_score for ${action.action_id}.`);
    }
    if (action.rank_score > previous + 0.0001) {
      fail('Next-best-action report must be sorted by descending rank_score.');
      break;
    }
    previous = action.rank_score;

    if (action.approval_required) {
      if (!action.approval_payload_path) {
        fail(`Approval-required action ${action.action_id} missing approval_payload_path.`);
        continue;
      }
      const payloadPath = path.join(ROOT, action.approval_payload_path);
      if (!fs.existsSync(payloadPath)) {
        fail(
          `Approval payload for ${action.action_id} does not exist at ${action.approval_payload_path}.`
        );
        continue;
      }
      const payload = JSON.parse(fs.readFileSync(payloadPath, 'utf8'));
      const payloadRequired = [
        'title',
        'source_plugin',
        'action_summary',
        'risk_class',
        'confidence_score',
        'requested_by',
      ];
      for (const field of payloadRequired) {
        if (payload[field] == null || payload[field] === '') {
          fail(`Approval payload ${action.approval_payload_path} missing "${field}".`);
        }
      }
    }
  }

  const md = readText('reports/exec/opspal-next-best-actions.md');
  if (!md.includes('OpsPal Next-Best Actions')) {
    fail('Next-best-action markdown summary missing expected heading.');
  }
  if (!md.includes('Shadow-Mode Triage')) {
    fail('Next-best-action markdown summary missing shadow-mode triage section.');
  }
}

