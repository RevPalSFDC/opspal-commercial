function parseCsv(csvText) {
  const rows = csvText
    .trim()
    .split('\n')
    .filter(Boolean)
    .map((line) => line.trim());

  const header = rows[0].split(',');
  const dataRows = rows.slice(1).map((line) => {
    // CSV generated by our writer does not include embedded commas in numeric fields.
    // Quoted fields are preserved by simple parser below.
    const out = [];
    let current = '';
    let inQuotes = false;
    for (let i = 0; i < line.length; i += 1) {
      const char = line[i];
      const next = line[i + 1];
      if (char === '"' && inQuotes && next === '"') {
        current += '"';
        i += 1;
      } else if (char === '"') {
        inQuotes = !inQuotes;
      } else if (char === ',' && !inQuotes) {
        out.push(current);
        current = '';
      } else {
        current += char;
      }
    }
    out.push(current);
    return out;
  });

  return { header, dataRows };
}

function validateCsv() {
  const csv = readText('reports/exec/opspal-gap-priority-matrix.csv');
  const { header, dataRows } = parseCsv(csv);

  const requiredHeader = [
    'id',
    'title',
    'category',
    'plugin_scope',
    'problem_statement',
    'evidence',
    'impact_score',
    'risk_reduction_score',
    'time_to_value_score',
    'effort_score',
    'governance_fit_score',
    'overall_score',
    'ninety_day_fit',
    'owner_suggested',
  ];

  if (requiredHeader.join(',') !== header.join(',')) {
    fail('Gap matrix CSV header does not match required contract.');
    return;
  }

  if (dataRows.length < 8) {
    fail('Gap matrix CSV should contain at least 8 opportunities.');
  }

  const scoreIndex = header.indexOf('overall_score');
  let previousScore = Number.POSITIVE_INFINITY;
  for (const row of dataRows) {
    const score = Number(row[scoreIndex]);
    if (!Number.isFinite(score)) {
      fail(`Non-numeric overall_score in CSV row: ${row[0]}`);
      continue;
    }
    if (score > previousScore + 0.0001) {
      fail('Gap matrix CSV is not sorted by descending overall_score.');
      break;
    }
    previousScore = score;
  }
}

