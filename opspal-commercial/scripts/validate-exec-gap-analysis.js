function validateRoiInstrumentationPayload() {
  const payload = JSON.parse(
    readText('reports/exec/opspal-initiative-roi-instrumentation.json')
  );

  if (payload.source_opportunity_id !== 'OPP-011') {
    fail('ROI instrumentation payload must link back to OPP-011.');
  }
  if (!payload.measurement_window || !payload.measurement_window.start_date) {
    fail('ROI instrumentation payload missing measurement_window.start_date.');
  }
  if (!payload.measurement_window || !payload.measurement_window.end_date) {
    fail('ROI instrumentation payload missing measurement_window.end_date.');
  }
  if (!Array.isArray(payload.initiatives) || payload.initiatives.length < 5) {
    fail('ROI instrumentation payload should include at least 5 initiatives.');
    return;
  }
  if (!payload.rollup || typeof payload.rollup !== 'object') {
    fail('ROI instrumentation payload missing rollup.');
  }

  let metricsTotal = 0;
  let highOrCriticalCount = 0;
  for (const initiative of payload.initiatives) {
    const requiredInitiativeFields = [
      'initiative_id',
      'linked_opportunity_id',
      'title',
      'owner_team',
      'risk_class',
      'metrics',
    ];
    for (const field of requiredInitiativeFields) {
      if (initiative[field] == null || initiative[field] === '') {
        fail(
          `ROI instrumentation initiative missing "${field}" (${initiative.initiative_id || 'unknown'}).`
        );
      }
    }

    if (!['low', 'medium', 'high', 'critical'].includes(initiative.risk_class)) {
      fail(
        `ROI instrumentation initiative has invalid risk_class "${initiative.risk_class}" (${initiative.initiative_id}).`
      );
    }
    if (['high', 'critical'].includes(initiative.risk_class)) {
      highOrCriticalCount += 1;
    }

    if (!Array.isArray(initiative.metrics) || initiative.metrics.length === 0) {
      fail(
        `ROI instrumentation initiative ${initiative.initiative_id} must include at least one metric.`
      );
      continue;
    }
    metricsTotal += initiative.metrics.length;

    for (const metric of initiative.metrics) {
      const requiredMetricFields = [
        'metric_id',
        'metric_name',
        'unit',
        'baseline_value',
        'target_value',
        'direction',
        'data_source',
        'reporting_artifact',
      ];
      for (const field of requiredMetricFields) {
        if (metric[field] == null || metric[field] === '') {
          fail(
            `ROI instrumentation metric missing "${field}" (${initiative.initiative_id}/${metric.metric_id || 'unknown'}).`
          );
        }
      }

      if (!['up', 'down'].includes(metric.direction)) {
        fail(
          `ROI instrumentation metric has invalid direction "${metric.direction}" (${initiative.initiative_id}/${metric.metric_id}).`
        );
      }

      if (
        typeof metric.baseline_value !== 'number' ||
        !Number.isFinite(metric.baseline_value)
      ) {
        fail(
          `ROI instrumentation metric baseline_value must be numeric (${initiative.initiative_id}/${metric.metric_id}).`
        );
      }
      if (
        typeof metric.target_value !== 'number' ||
        !Number.isFinite(metric.target_value)
      ) {
        fail(
          `ROI instrumentation metric target_value must be numeric (${initiative.initiative_id}/${metric.metric_id}).`
        );
      }
    }
  }

  if (payload.rollup && typeof payload.rollup === 'object') {
    const requiredRollupFields = [
      'initiatives_total',
      'metrics_total',
      'high_or_critical_risk_initiatives',
    ];
    for (const field of requiredRollupFields) {
      if (payload.rollup[field] == null || payload.rollup[field] === '') {
        fail(`ROI instrumentation rollup missing "${field}".`);
      }
      if (typeof payload.rollup[field] !== 'number' || payload.rollup[field] < 0) {
        fail(`ROI instrumentation rollup ${field} must be non-negative numeric.`);
      }
    }
    if (payload.rollup.initiatives_total !== payload.initiatives.length) {
      fail(
        `ROI instrumentation rollup initiatives_total mismatch (${payload.rollup.initiatives_total} !== ${payload.initiatives.length}).`
      );
    }
    if (payload.rollup.metrics_total !== metricsTotal) {
      fail(
        `ROI instrumentation rollup metrics_total mismatch (${payload.rollup.metrics_total} !== ${metricsTotal}).`
      );
    }
    if (payload.rollup.high_or_critical_risk_initiatives !== highOrCriticalCount) {
      fail(
        `ROI instrumentation rollup high_or_critical_risk_initiatives mismatch (${payload.rollup.high_or_critical_risk_initiatives} !== ${highOrCriticalCount}).`
      );
    }
  }

  if (!payload.execution_snapshot || typeof payload.execution_snapshot !== 'object') {
    fail('ROI instrumentation payload missing execution_snapshot.');
    return;
  }
  const snapshot = payload.execution_snapshot;
  if (!snapshot.kpi_contract || typeof snapshot.kpi_contract !== 'object') {
    fail('ROI instrumentation execution_snapshot missing kpi_contract.');
  } else {
    const kpiContract = snapshot.kpi_contract;
    const requiredKpiFields = [
      'initiatives_total',
      'initiatives_with_metrics',
      'metrics_total',
      'metrics_with_evidence',
      'coverage_ratio',
    ];
    for (const field of requiredKpiFields) {
      if (kpiContract[field] == null || kpiContract[field] === '') {
        fail(`ROI instrumentation kpi_contract missing "${field}".`);
      }
    }
    for (const field of [
      'initiatives_total',
      'initiatives_with_metrics',
      'metrics_total',
      'metrics_with_evidence',
    ]) {
      if (typeof kpiContract[field] !== 'number' || kpiContract[field] < 0) {
        fail(`ROI instrumentation kpi_contract ${field} must be non-negative numeric.`);
      }
    }
    if (
      typeof kpiContract.coverage_ratio !== 'number' ||
      kpiContract.coverage_ratio < 0 ||
      kpiContract.coverage_ratio > 1
    ) {
      fail('ROI instrumentation kpi_contract coverage_ratio must be numeric in [0,1].');
    }

    const initiativesWithMetrics = payload.initiatives.filter(
      (initiative) => Array.isArray(initiative.metrics) && initiative.metrics.length > 0
    ).length;
    let metricsWithEvidence = 0;
    for (const initiative of payload.initiatives) {
      for (const metric of initiative.metrics || []) {
        if (metric.data_source && metric.reporting_artifact) {
          metricsWithEvidence += 1;
        }
      }
    }
    const expectedCoverageRatio =
      metricsTotal === 0 ? 0 : toFixed2(metricsWithEvidence / metricsTotal);
    if (kpiContract.initiatives_total !== payload.initiatives.length) {
      fail(
        `ROI instrumentation kpi_contract initiatives_total mismatch (${kpiContract.initiatives_total} !== ${payload.initiatives.length}).`
      );
    }
    if (kpiContract.initiatives_with_metrics !== initiativesWithMetrics) {
      fail(
        `ROI instrumentation kpi_contract initiatives_with_metrics mismatch (${kpiContract.initiatives_with_metrics} !== ${initiativesWithMetrics}).`
      );
    }
    if (kpiContract.metrics_total !== metricsTotal) {
      fail(
        `ROI instrumentation kpi_contract metrics_total mismatch (${kpiContract.metrics_total} !== ${metricsTotal}).`
      );
    }
    if (kpiContract.metrics_with_evidence !== metricsWithEvidence) {
      fail(
        `ROI instrumentation kpi_contract metrics_with_evidence mismatch (${kpiContract.metrics_with_evidence} !== ${metricsWithEvidence}).`
      );
    }
    if (Math.abs(kpiContract.coverage_ratio - expectedCoverageRatio) > 0.0001) {
      fail(
        `ROI instrumentation kpi_contract coverage_ratio mismatch (${kpiContract.coverage_ratio} !== ${expectedCoverageRatio}).`
      );
    }
  }

  if (!snapshot.runtime_handoff || typeof snapshot.runtime_handoff !== 'object') {
    fail('ROI instrumentation execution_snapshot missing runtime_handoff.');
  } else {
    const runtimeHandoff = snapshot.runtime_handoff;
    const requiredRuntimeFields = [
      'execution_checklist_detected',
      'execution_checklist_path',
      'strategy_dashboard_payload_detected',
      'strategy_dashboard_payload_path',
    ];
    for (const field of requiredRuntimeFields) {
      if (runtimeHandoff[field] == null || runtimeHandoff[field] === '') {
        fail(`ROI instrumentation runtime_handoff missing "${field}".`);
      }
    }
    if (typeof runtimeHandoff.execution_checklist_detected !== 'boolean') {
      fail('ROI instrumentation runtime_handoff execution_checklist_detected must be boolean.');
    }
    if (
      runtimeHandoff.execution_checklist_path != null &&
      typeof runtimeHandoff.execution_checklist_path !== 'string'
    ) {
      fail(
        'ROI instrumentation runtime_handoff execution_checklist_path must be a string or null.'
      );
    }
    if (
      typeof runtimeHandoff.execution_checklist_path === 'string' &&
      runtimeHandoff.execution_checklist_path !== '' &&
      !runtimeHandoff.execution_checklist_path.startsWith('reports/exec/runtime/')
    ) {
      fail(
        'ROI instrumentation runtime_handoff execution_checklist_path must be rooted under reports/exec/runtime.'
      );
    }
    if (
      runtimeHandoff.execution_checklist_detected &&
      !runtimeHandoff.execution_checklist_path
    ) {
      fail(
        'ROI instrumentation runtime_handoff execution_checklist_detected=true requires execution_checklist_path.'
      );
    }

    if (typeof runtimeHandoff.strategy_dashboard_payload_detected !== 'boolean') {
      fail(
        'ROI instrumentation runtime_handoff strategy_dashboard_payload_detected must be boolean.'
      );
    }
    if (
      typeof runtimeHandoff.strategy_dashboard_payload_path !== 'string' ||
      !runtimeHandoff.strategy_dashboard_payload_path.startsWith('reports/exec/')
    ) {
      fail(
        'ROI instrumentation runtime_handoff strategy_dashboard_payload_path must be rooted under reports/exec/.'
      );
    }
    const expectedDashboardDetected = fs.existsSync(
      path.join(ROOT, runtimeHandoff.strategy_dashboard_payload_path)
    );
    if (runtimeHandoff.strategy_dashboard_payload_detected !== expectedDashboardDetected) {
      fail(
        `ROI instrumentation runtime_handoff strategy_dashboard_payload_detected mismatch (${runtimeHandoff.strategy_dashboard_payload_detected} !== ${expectedDashboardDetected}).`
      );
    }
  }

  if (!snapshot.completion_gate || typeof snapshot.completion_gate !== 'object') {
    fail('ROI instrumentation execution_snapshot missing completion_gate.');
  } else {
    const gate = snapshot.completion_gate;
    const gateFields = [
      'kpi_contract_ready',
      'runtime_handoff_ready',
      'ready_for_phase2_reporting',
    ];
    for (const field of gateFields) {
      if (gate[field] == null || gate[field] === '') {
        fail(`ROI instrumentation completion_gate missing "${field}".`);
      }
      if (typeof gate[field] !== 'boolean') {
        fail(`ROI instrumentation completion_gate ${field} must be boolean.`);
      }
    }

    const kpiContract = snapshot.kpi_contract || {};
    const runtimeHandoff = snapshot.runtime_handoff || {};
    const expectedKpiReady =
      kpiContract.initiatives_total > 0 &&
      kpiContract.initiatives_with_metrics === kpiContract.initiatives_total &&
      kpiContract.metrics_total > 0 &&
      kpiContract.metrics_with_evidence === kpiContract.metrics_total;
    if (gate.kpi_contract_ready !== expectedKpiReady) {
      fail(
        `ROI instrumentation completion_gate kpi_contract_ready mismatch (${gate.kpi_contract_ready} !== ${expectedKpiReady}).`
      );
    }

    const expectedRuntimeReady =
      runtimeHandoff.execution_checklist_detected &&
      runtimeHandoff.strategy_dashboard_payload_detected;
    if (gate.runtime_handoff_ready !== expectedRuntimeReady) {
      fail(
        `ROI instrumentation completion_gate runtime_handoff_ready mismatch (${gate.runtime_handoff_ready} !== ${expectedRuntimeReady}).`
      );
    }

    const expectedReadyForPhase2 = expectedKpiReady && expectedRuntimeReady;
    if (gate.ready_for_phase2_reporting !== expectedReadyForPhase2) {
      fail(
        `ROI instrumentation completion_gate ready_for_phase2_reporting mismatch (${gate.ready_for_phase2_reporting} !== ${expectedReadyForPhase2}).`
      );
    }
  }
}

