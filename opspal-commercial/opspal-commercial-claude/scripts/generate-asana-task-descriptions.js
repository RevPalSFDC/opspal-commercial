#!/usr/bin/env node

/**
 * Generate formatted Asana task descriptions from fix plans
 * Outputs markdown ready to copy into Asana
 */

const fs = require('fs');
const path = require('path');

// Read fix plans
const fixPlansPath = path.join(__dirname, '../reports/fix-plans-2025-10-26.json');
const fixPlansData = JSON.parse(fs.readFileSync(fixPlansPath, 'utf8'));
const fixPlans = fixPlansData.fix_plans;

function calculateDueDate(effortHours) {
  const today = new Date();
  let daysToAdd;
  let label;

  if (effortHours < 8) {
    daysToAdd = 14;
    label = '2 weeks';
  } else if (effortHours < 16) {
    daysToAdd = 28;
    label = '4 weeks';
  } else {
    daysToAdd = 42;
    label = '6 weeks';
  }

  const dueDate = new Date(today.getTime() + daysToAdd * 24 * 60 * 60 * 1000);
  return {
    date: dueDate.toISOString().split('T')[0],
    label: label
  };
}

function getActionVerb(solutionType) {
  const verbs = {
    'api-client-abstraction': 'Build',
    'validation-framework': 'Implement',
    'flow-logic-validator': 'Create',
    'test-coverage-framework': 'Build',
    'deployment-tracking-system': 'Build',
    'quality-assurance-framework': 'Implement',
    'expectation-management-framework': 'Create',
    'critical-thinking-framework': 'Build',
    'investigation-required': 'Investigate'
  };
  return verbs[solutionType] || 'Implement';
}

function formatTaskMarkdown(fixPlan, taskNumber) {
  const rca = fixPlan.root_cause_analysis_5_layer;
  const solution = fixPlan.solution;
  const alternatives = fixPlan.alternatives_considered;
  const cohortReflectionCount = rca.recurrence_context.recurrence_count;
  const dueDate = calculateDueDate(fixPlan.estimated_effort_hours);
  const actionVerb = getActionVerb(fixPlan.solution.type);
  const title = `${actionVerb} ${fixPlan.solution.title}`;

  const weeklyHours = (fixPlan.expected_roi_annual / 52 / 100).toFixed(1);
  const paybackMonths = ((fixPlan.estimated_effort_hours / (fixPlan.expected_roi_annual / 2080)) * 12).toFixed(1);

  return `## Task ${taskNumber}: ${title}

**Cohort ID:** ${fixPlan.cohort_id}
**Fix Plan ID:** ${fixPlan.fix_plan_id}
**Due Date:** ${dueDate.date} (${dueDate.label})
**Priority:** P0 → High
**Tags:** ${fixPlan.taxonomy}, reflection-cohort, ${fixPlan.solution.type}
**Estimated ROI:** $${fixPlan.expected_roi_annual.toLocaleString()}/year
**Effort:** ${fixPlan.estimated_effort_hours} hours

### Description

## 🔴 The Issue(s)
${rca.layer_1_symptom.description}

- **Error Pattern**: ${rca.layer_1_symptom.error_pattern || rca.layer_1_symptom.description}
- **Frequency**: ${cohortReflectionCount} occurrences
- **Impact**: ${rca.layer_1_symptom.impact}
- **Taxonomy**: ${fixPlan.taxonomy}

## 🔬 Root Cause Analysis (5-Why)
**Why 1:** ${rca.layer_1_symptom.description}
**Why 2:** ${rca.layer_2_immediate_cause.description}
**Why 3:** ${rca.layer_3_contributing_factors.join(', ')}
**Why 4:** ${rca.layer_4_safeguard_gaps.join(', ')}
**Why 5 (Root Cause):** ${rca.layer_5_systemic_root.root_cause}

**Contributing factors:**
${rca.layer_3_contributing_factors.map(f => `- ${f}`).join('\n')}

**Why safeguards failed:**
${rca.layer_4_safeguard_gaps.map(g => `- ${g}`).join('\n')}

## 📊 The Impact
- **Time wasted**: ~${weeklyHours} hours/week
- **Annual ROI of fix**: $${fixPlan.expected_roi_annual.toLocaleString()}
- **Implementation effort**: ${fixPlan.estimated_effort_hours} hours
- **Payback period**: ${paybackMonths} months
- **Success probability**: ${(fixPlan.success_probability * 100).toFixed(0)}%
- **Severity**: ${fixPlan.priority}
- **Reflections in cohort**: ${cohortReflectionCount}

## ✅ The Solution
**Type:** ${solution.type}
**Approach:** ${solution.description}

**Prevention Focus:** ${solution.prevention_focus}

**Components to be created:**
${solution.components_created.map(c => `- ${c}`).join('\n')}

**Components to be modified:**
${solution.components_modified.map(c => `- ${c}`).join('\n')}

## 🤔 Alternative Solutions Considered

${alternatives.map(alt => `### ${alt.title}
**Prevention Depth:** ${alt.prevention_depth}
**Effort:** ${alt.effort_hours} hours | **ROI:** $${alt.expected_roi.toLocaleString()}/year

**Pros:** ${alt.pros.join(', ')}

**Cons:** ${alt.cons.join(', ')}

**Decision:** ${alt.why_not_chosen || alt.why_chosen}
`).join('\n---\n\n')}

## 📎 Related Reflections
- **Cohort ID**: ${fixPlan.cohort_id}
- **Fix Plan ID**: ${fixPlan.fix_plan_id}
- **First Occurrence**: ${rca.recurrence_context.first_occurrence}
- **Recurrence Severity**: ${rca.recurrence_context.recurrence_severity}

## 🎯 Success Criteria
${fixPlan.success_criteria.map(c => `- ${c}`).join('\n')}

**Verification Required:**
${solution.verification_required.map(v => `- ${v}`).join('\n')}

---
**Generated by:** OpsPal Reflection System
**Fix Depth:** ${rca.fix_depth_required}

---

`;
}

// Generate all tasks
const markdownOutput = fixPlans.map((fp, idx) => formatTaskMarkdown(fp, idx + 1)).join('\n');

// Write to file
const outputPath = path.join(__dirname, '../reports/asana-task-descriptions-complete.md');
const header = `# Asana Tasks Ready to Create - P0 Critical Fix Plans

**Generation Date:** ${new Date().toISOString().split('T')[0]}
**Asana Project:** OpsPal - Reflection Improvements (GID: 1211617834659194)
**Total Tasks:** ${fixPlans.length}
**Total Estimated ROI:** $${fixPlansData.summary.total_expected_roi_annual.toLocaleString()}/year
**Total Effort:** ${fixPlansData.summary.total_estimated_hours} hours

## Instructions

1. Open Asana project: https://app.asana.com/0/1211617834659194
2. For each task below, create a new task in Asana
3. Copy the task title (e.g., "[Reflection Cohort] Build HubSpot API Client...")
4. Copy the entire "Description" section into the Asana task description
5. Set the due date as indicated
6. Set priority to "High" (all P0 tasks)
7. Add the tags listed
8. Add custom field values if available: estimated_roi and effort_hours

---

`;

fs.writeFileSync(outputPath, header + markdownOutput);
console.log(`✅ Generated ${fixPlans.length} task descriptions`);
console.log(`📄 Saved to: ${outputPath}`);
console.log(`\n📋 File size: ${(fs.statSync(outputPath).size / 1024).toFixed(1)} KB`);
