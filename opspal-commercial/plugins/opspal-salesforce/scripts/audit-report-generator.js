    /**
     * Get JavaScript for HTML report
     */
    getJavaScript() {
        return `
        document.addEventListener('DOMContentLoaded', function() {
            // Collapsible sections
            const collapsibles = document.getElementsByClassName('collapsible');
            
            for (let i = 0; i < collapsibles.length; i++) {
                collapsibles[i].addEventListener('click', function() {
                    this.classList.toggle('active');
                    const content = this.nextElementSibling;
                    content.classList.toggle('show');
                });
            }
            
            // Animate progress bars
            const progressBars = document.querySelectorAll('.progress-fill');
            progressBars.forEach(bar => {
                const width = bar.getAttribute('data-width');
                setTimeout(() => {
                    bar.style.width = width + '%';
                }, 100);
            });
            
            // Add click-to-expand for detailed findings
            const findingCards = document.querySelectorAll('.finding-card');
            findingCards.forEach(card => {
                card.style.cursor = 'pointer';
                card.addEventListener('click', function() {
                    const details = this.querySelector('.finding-details');
                    if (details) {
                        details.style.display = details.style.display === 'none' ? 'block' : 'none';
                    }
                });
            });
        });`;
    }

    /**
     * Generate executive summary
     */
    generateExecutiveSummary() {
        const metadata = this.data.detailedFindings || {};
        const analysis = this.data.analysis || {};
        
        return {
            objectName: this.data.metadata?.objectName || 'Unknown',
            overallHealth: this.data.scores?.overall || 0,
            totalFields: metadata.fields?.total || 0,
            customFields: metadata.fields?.custom || 0,
            unusedFields: metadata.fieldUsage?.unused || 0,
            triggers: metadata.triggers?.count || 0,
            validationRules: metadata.validationRules?.count || 0,
            flows: metadata.automation?.flows?.count || 0,
            records: metadata.recordMetrics?.totalRecords || 0,
            criticalIssues: this.countIssuesByPriority(1),
            importantIssues: this.countIssuesByPriority(2),
            minorIssues: this.countIssuesByPriority(3)
        };
    }

    /**
     * Count issues by priority
     */
    countIssuesByPriority(priority) {
        return (this.data.recommendations || [])
            .filter(r => r.priority === priority)
            .length;
    }

